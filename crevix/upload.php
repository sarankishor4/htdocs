$message = '';

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $title = $conn->real_escape_string(trim($_POST['title']));
    $desc = $conn->real_escape_string(trim($_POST['description']));
    $category = $conn->real_escape_string($_POST['category']);
    $user_id = $_SESSION['user_id'];

    if (isset($_FILES['media_file']) && $_FILES['media_file']['error'] == 0) {
        $file = $_FILES['media_file'];
        $filename = time() . '_' . preg_replace('/[^a-zA-Z0-9._-]/', '', basename($file['name']));
        $target_dir = "uploads/media/";
        $target_file = $target_dir . $filename;

        $ext = strtolower(pathinfo($target_file, PATHINFO_EXTENSION));
        $type = in_array($ext, ['mp4','webm','ogg','mov','avi']) ? 'video' : 'photo';

        // Handle thumbnail
        $thumb_path = '';
        if (isset($_POST['thumbnail_data']) && !empty($_POST['thumbnail_data'])) {
            $thumb_data = $_POST['thumbnail_data'];
            $thumb_data = str_replace('data:image/png;base64,', '', $thumb_data);
            $thumb_data = str_replace(' ', '+', $thumb_data);
            $thumb_decoded = base64_decode($thumb_data);
            $thumb_name = 'thumb_' . time() . '.png';
            $thumb_path = 'uploads/media/' . $thumb_name;
            file_put_contents($thumb_path, $thumb_decoded);
        }

        if (move_uploaded_file($file['tmp_name'], $target_file)) {
            $sql = "INSERT INTO media (user_id, title, description, file_path, thumbnail, type, category) VALUES ('$user_id', '$title', '$desc', '$target_file', '$thumb_path', '$type', '$category')";
            if ($conn->query($sql)) {
                $message = "<div class='alert alert-success'>✅ Upload successful! <a href='dashboard.php' style='color:var(--gold)'>View in Dashboard</a></div>";
            } else {
                $message = "<div class='alert alert-error'>Database error: " . $conn->error . "</div>";
            }
        } else {
            $message = "<div class='alert alert-error'>Failed to move uploaded file.</div>";
        }
    } else {
        $message = "<div class='alert alert-error'>Please select a file.</div>";
    }
}
