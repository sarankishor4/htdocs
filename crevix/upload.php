<script>
// Drag & Drop + File Select
const dropZone = document.getElementById('dropZone');
const fileInput = document.getElementById('fileInput');
const thumbPreview = document.getElementById('thumbPreview');
const thumbCanvas = document.getElementById('thumbCanvas');
const thumbnailData = document.getElementById('thumbnailData');

dropZone.addEventListener('click', () => fileInput.click());
dropZone.addEventListener('dragover', (e) => { e.preventDefault(); dropZone.classList.add('dragover'); });
dropZone.addEventListener('dragleave', () => dropZone.classList.remove('dragover'));
dropZone.addEventListener('drop', (e) => {
    e.preventDefault();
    dropZone.classList.remove('dragover');
    fileInput.files = e.dataTransfer.files;
    handleFile(e.dataTransfer.files[0]);
});
fileInput.addEventListener('change', () => { if(fileInput.files[0]) handleFile(fileInput.files[0]); });

function handleFile(file) {
    dropZone.innerHTML = '<p style="color:var(--gold); font-weight:600;">✅ ' + file.name + ' (' + (file.size/1024/1024).toFixed(2) + ' MB)</p>';

    // Auto-generate thumbnail from video
    if (file.type.startsWith('video/')) {
        const video = document.createElement('video');
        video.preload = 'metadata';
        video.muted = true;
        video.playsInline = true;

        video.addEventListener('loadeddata', function() {
            // Seek to 2 seconds for a better frame
            video.currentTime = Math.min(2, video.duration * 0.25);
        });

        video.addEventListener('seeked', function() {
            thumbCanvas.width = video.videoWidth;
            thumbCanvas.height = video.videoHeight;
            const ctx = thumbCanvas.getContext('2d');
            ctx.drawImage(video, 0, 0, thumbCanvas.width, thumbCanvas.height);
            const dataURL = thumbCanvas.toDataURL('image/png');
            thumbPreview.src = dataURL;
            thumbPreview.classList.add('show');
            thumbnailData.value = dataURL;
            URL.revokeObjectURL(video.src);
        });

        video.src = URL.createObjectURL(file);
    } else if (file.type.startsWith('image/')) {
        // For images, use the image itself as thumbnail
        const reader = new FileReader();
        reader.onload = function(e) {
            thumbPreview.src = e.target.result;
            thumbPreview.classList.add('show');
            thumbnailData.value = e.target.result;
        };
        reader.readAsDataURL(file);
    }
}
</script>

